    @staticmethod
    def devolver_info_equipos():
        info_equipo_seleccionado = []
        for equipo in Teams.obtener_info_equipos():
            info_equipo_seleccionado.append({"id": equipo["id"], "name": equipo["name"]})
        return info_equipo_seleccionado

    # Método que retorna el id y name de un equipo específico
    @staticmethod
    def devolver_info_equipo_especifico(id_team):
        info_equipo_seleccionado = []
        for equipo in Teams.obtener_info_equipos():
            if equipo["id"] == id_team:
                info_equipo_seleccionado.append({"id": equipo["id"], "name": equipo["name"]})
        return info_equipo_seleccionado

