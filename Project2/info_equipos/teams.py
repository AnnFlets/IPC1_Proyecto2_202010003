    @staticmethod
    def devolver_estampitas(id_team):
        estampitas = []
        for equipo in Teams.obtener_info_equipos():
            if equipo["id"] == id_team:
                for estampita in equipo["elements"]:
                    estampitas.append(estampita)
        return estampitas

